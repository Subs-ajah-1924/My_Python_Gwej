#aritmatika apik
a=10
b=6
#penjumlahan (+)
hasil= a + b
print (a,'+',b,'=', hasil)

#pengurangan (-)
hasil= a - b
print (a,'-',b,'=', hasil)

#perkalian (*)
hasil= a * b
print (a,'*',b,'=', hasil)

#pembagian (/)
hasil= a / b
print (a,'/',b,'=', hasil)

#eksponen/pangkat (**)
hasil= a ** b
print (a,'**',b,'=', hasil)

#modulus/sisa pembagian (%)
hasil= a % b
print (a,'%',b,'=', hasil)

#floor division/kebagi piro (//)
hasil= a // b
print (a,'//',b,'=', hasil)


